import java.util.*;
public class Relationaloperators{
public static void main(String args[])
{
	Scanner sc=new Scanner(System.in);
	int a,b;
	a=sc.nextInt();
	b=sc.nextInt();
	System.out.println(a>=b);
	System.out.println(a<=b);
	char ch1='a';
	char ch2='b';
	System.out.println(ch1>=ch2);
}
}
